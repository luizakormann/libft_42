#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		size;
	t_list	*tmp;

	size = 0;
	tmp = lst;
	while (!tmp)
	{
		size++;
		tmp = tmp->next;
	}
	return (size);
}
